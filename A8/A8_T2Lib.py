def add(PAddend1 : float, PAddend2 : float):
    Sum = PAddend1 + PAddend2
    return Sum

def substract(PMinuend : float, PSubtrahend : float):
    Sub = float(PMinuend - PSubtrahend)
    return Sub

def mutiply(PMultiplicant : float, PMultiplier : float):
    Sum = float(PMultiplicant * PMultiplier)
    return Sum

def divide(PDividend : float, PDivisor : float):
    Sum = float(PDividend / PDivisor)
    return Sum